def add(number1, number2)
	c = number1 + number2
	return c
end

def subtract(number3, number4)
	d = number3 - number4
	return d
end

def sum(a)
	return a.sum 
end

def multiply(number5, number6)
	g = number5 * number6
	return g
end

def power(number8, number9)
	y = number8 ** number9
	return y
end

def factorial(number7)
	f = Math.gamma(number7 + 1)
	return f
end

